package com.example.fixx.NavigationBar.HomeScreen

import android.view.View
import android.widget.GridView
import android.widget.TextView
import com.example.fixx.R


public class NotificationCounter (view: View){
    private val notificationNumber: TextView
    private val MAX_NUMBER = 99
    private var notification_counter = 0

    init {
        notificationNumber = view.findViewById(R.id.homefragment_notificationcounter_text_view)
    }

    fun increaseNumber() {
        notification_counter++
        if (notification_counter > MAX_NUMBER) {
            notificationNumber.text = "+99"
        } else {
            notificationNumber.text = notification_counter.toString()
        }
    }
}